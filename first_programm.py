from urllib2 import urlopen        
u = urlopen("http://python.org")   
words = {}                          
                                    
for line in u:          
    line = line.strip(" \n")  
    for word in line.split(" "): 
        try:                            
            words[word] += 1            
        except KeyError:               
            words[word] = 1             



pairs = words.items()              
                                   
pairs.sort(key=lambda x: x[1], reverse=True)  

for p in pairs[:10]:                
    print(p[0], p[1])


